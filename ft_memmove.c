#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (dest == NULL && src == NULL)
		return (NULL);
	i = 0;
	if (dest <= src)
	{
		while (i < n)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < n)
		{
			((char *)dest)[n - 1 - i] = ((char *)src)[n - 1 - i];
			i++;
		}
	}
	return (dest);
}
